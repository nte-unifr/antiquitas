Welche Aktivitäten zeichnen den Lebensstil des Romanianus aus? Welches ist das entscheidende 
Kriterium, das Voraussetzung sowohl für den Aufstieg im cursus honorum als auch für 
die Repräsentation des sozialen Ranges von Romanianus ist?</td><td></td>
<td valign="top">Die zur Bekleidung magistratischer Ämter Ernannten hatten traditionell 
Spenden und Spiele zu veranstalten. Sie hatten die Euergesien anlässlich ihrer Wahl in ein 
öffentliches Amt zu leisten. Diese Vergnügungen nahmen einen wichtigen Platz im 
täglichen Leben ein. Damit standen sie in Konkurrenz zur christlichen Kirche, namentlich den 
Gottesdiensten. Der Bischof Augustinus war sich dessen bewusst:<br><br>
<i>Quam multos enim hodie fratres nostros cogitamus et plangimus ire in vanitates et insanias mendaces, neglegere quo vocati sunt!</i> (Ennarationes in psalmos, 50, 1)<br><br>
Er hält nicht zurück mit Kritik an den Euergeten, die diese kostspieligen Vergnügen 
finanzieren:<br><br>
<i>Habe aurum, dicit tibi deus, dedi tibi, bene utere. Ornari vis auro,
orna potius aurum; honorem vis, decus vis ab auro, decus praesta auro,
ne sis dedecus auri. Aurum habet: scortatur, fornicatur, luxuriatur,
edit pompacticos ludos, insana munera donat histrionibus, esurientibus
pauperibus non donat, non est decus auri. </i>(Serm. 21, 8)<br><br>
Worin besteht die Kritik des christlichen Schriftstellers am Euergetismus der Honoratioren? 
