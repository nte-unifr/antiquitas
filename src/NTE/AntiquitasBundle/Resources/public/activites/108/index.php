Maximin ist jedoch nicht der einzige Adlige, der sich Ammians Kritik gefallen lassen muss:<br><br>
(…) <i>et
ubique patrum reuerenda cum auctoritate canities populique Romani nomen
circumspectum et uerecundum. Sed laeditur hic coetuum magnificus
splendor leuitate paucorum incondita, ubi nati sunt, non reputantium,
sed tamquam indulta licentia uitiis ad errores lapsorum atque lasciuiam.</i> (Amm. 14.6.6)<br><br>
Wodurch sollten sich nach Ammianus Marcellinus‘ Dafürhalten die Vertreter der <i>nobilitas</i> auszeichnen? Indirekte Hinweise lassen sich auch seiner Kritik am Verhalten von Maximin entnehmen.

