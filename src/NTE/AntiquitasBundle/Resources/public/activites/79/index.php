<font color="#660000" size="+1"><b>Kapitel 2: Einstieg in die Wissenschaft II <br>
<br>

            Augustinus</b></font><br>
<br>
Kehren Sie zurück zu ihrer 
              "virtuellen" Seminararbeit über Augustin. Sicherlich 
              haben Sie bereits einen chronologischen und geographischen Überblick 
              über die Alte Geschichte gewonnen und ist es Ihnen ebenso gelungen, 
              Augustin zeitlich (356-430) und geographisch (Hippo, Nordfrika) 
              richtig einzuordnen. Informieren Sie sich nun über Augustin 
              und seinen Weg zum Bischof. Wie eignen Sie sich rasch und zuverlässig 
              Kenntnisse über das frühe Christentum und seine Exponenten, 
              über die in spätrömischer Zeit sich ausbildenden 
              und festigenden kirchlichen Ämter an? Wo sind kompakte Informationen 
              über bedeutende Häresien – was bedeutet "Häresie" 
              genau? - und deren Vertreter zu finden, die Augustin bekämpft 
              hat?<br>
              Wenden Sie sich dann <i>ad fontes</i>! Gewinnen Sie den Überblick 
              über den "Bazar", den Augustin hinterlassen hat, 
              und versuchen Sie zunächst, anhand der Titel der einzelnen 
              Werke für Ihr Thema relevante Schriften zu finden. Suchen Sie 
              geeignete Medien, die Ihnen einen inhaltlichen Überblick über 
              diese Schriften geben. Wenn Sie nicht völlig abseits der Wege 
              gehen, wissen Sie dann auch bereits, wie die entsprechenden Werke 
              gewöhnlich zitiert werden. Wählen Sie unter den vorhandenen 
              Editionen die geeigneten aus! Wenn Sie sich nun aufmachen, ein Kapitel 
              aus De civitate Dei oder eine Predigt Augustins nicht lediglich 
              in einer Übersetzung kursorisch zu lesen, sondern den betreffenden 
              Text auch in seiner ursprünglichen lateinischen Fassung zu 
              analysieren und zu verstehen: welche Hilfsmittel ziehen sie hinzu?<br>
              Was können Sie auf "direktem" Weg – durch die 
              Quellen, die aus dieser Zeit auf uns gekommen sind - neben Augustins 
              eigenen Aussagen über die spätrömische Gesellschaft 
              erfahren? Gibt es Autoren, die im 4. und 5. Jahrhundert sich als 
              "Geschichtsschreiber" verstehend berichtet haben und uns 
              heute noch zugänglich sind?<br>
              Wenn Sie zwar Berichte das Römische Reich in der Spätantike 
              betreffend gefunden haben, es aber der Zufall will, dass über 
              das nordafrikanische Christentum im ausgehenden 4. bzw. beginnenden 
              5. Jahrhundert keine Berichte eines Historikers wie Ammianus Marcellinus 
              vorliegen: Welche weiteren Mittel haben Sie als HistorikerIn zur 
              Hand, um Spuren dieses Mannes zu finden, der nach langer Suche das 
              höchste Gut, das <i>summum bonum </i>gefunden hat:<br>

<br>
"Denn wir sehen unser Gut 
              noch nicht, müssen es also glaubend suchen, können auch 
              nicht aus eigener Kraft recht leben, wenn der nicht unserm Glauben 
              und Gebet beisteht, der uns den Glauben einflösste, dass wir 
              seine Hilfe nötig haben. Jene aber, die meinen, das Endziel 
              des Guten und Bösen sei in diesem Leben zu finden, suchen das 
              höchste Gut entweder im Leibe oder in der Seele oder in beiden. 
              Ich will es etwas ausführlicher darlegen. Sie suchen es entweder 
              in der Lust oder in der Tugend oder in beiden, oder aber entweder 
              in der Ruhe oder in der Tugend oder in beiden, oder aber entweder 
              in der Lust und Ruhe zugleich oder in der Tugend oder in beiden, 
              oder endlich entweder in den ursprünglichen Naturgütern 
              oder in der Tugend oder in beiden; in jedem Fall wollen sie in erstaunlicher 
              Verblendung hier glückselig sein und durch sich selbst glückselig 
              werden. Aber die Wahrheit spottet ihrer und spricht durch den Propheten: 
              'Der Herr kennt die Gedanken der Menschen', oder wie der Apostel 
              dies Schriftwort wiedergibt: 'Der Herr kennt der Weisen Gedanken, 
              dass sie eitel sind.'" (Aug. civ. 19, 4)<br><br><br>


