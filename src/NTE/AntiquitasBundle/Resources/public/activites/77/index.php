   <br><br>Augustinus</b></font><br> <br>Steigen 
              Sie ein in die Vorbereitungen einer "Scheinseminararbeit" 
              mit dem Titel "Der heilige Augustin als Kirchenpolitiker"! 
              Hier geht es nur um das WIE, um die Kenntnisse der Beschaffung wichtiger 
              Informationen. – Wo setzen Sie an? Wie finden Sie sich im weiten 
              Feld "Alte Geschichte" zurecht? Wie gewinnen Sie den Überblick? 
              Welche Informationen benötigen Sie, um diesen christlichen 
              Schriftsteller zeitlich und geographisch richtig einzuordnen, welche 
              Epochen und Regionen können Sie vorab ausschliessen? In welche 
              Gebiete führen Sie die ersten Schritte? Mit welchen Medien 
              arbeiten Sie, um sich Kenntnisse über dieses Thema anzueignen? 
              Wo können Sie was über Augustin und seine Zeit herausfinden, 
              ohne bereits des Kirchenvaters <i>opus magnum</i> "Über 
              den Gottesstaat" und Berichte anderer Autoren aus dieser Zeit 
              gelesen zu haben? Wie gelangen Sie zu den neuesten Untersuchungen 
              zu einem Kirchenvater, der sich u.a. folgendem Unternehmen verschrieben 
              hat:<br>
<br>
"…möchte 
              ich den ruhmreichen Gottesstaat verteidigen, ihn, der in dieser 
              Weltzeit unter Gottlosen pilgert und 'im Glauben lebt', ihn, der 
              in der ewigen Behausung seine sichere Ruhestatt finden wird, deren 
              'er jetzt in Geduld wartet, bis die Gerechtigkeit sich wendet zum 
              Gericht', und die er dereinst herrlich erlangen wird, wenn der Endsieg 
              errungen und vollkommener Friede eingekehrt ist. Ich möchte 
              ihn verteidigen gegen diejenigen, die seinem Begründer ihre 
              Götter vorziehen." (Aug. civ. 1, praef.)


