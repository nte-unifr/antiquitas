<strong>Innovation und Tradition</strong><br>

<p>Die spätrömische Zeit ist eine Zeit des 
Überganges, in der sich vielfältige, für die europäische 
Geschichte bedeutende Veränderungen auf politischer, gesellschaftlicher, 
kultureller und religiöser Ebene abspielen. Dabei darf indes 
nicht übersehen werden, dass in dieser Zeit der Transformation 
auch Bestehendes sich zu erhalten vermag, Tradition weiterhin gepflegt 
und gelebt wird und es auch nicht an Versuchen fehlt, Verschüttetes 
wieder auszugraben. Überlegen Sie sich, welche Elemente in 
ein Schema mit den Kategorien Innovation, Tradition und Restauration 
und den Sparten Politik, Gesellschaft, Kultur und Religion in spätrömischer Zeit passen!</p>

