<font color="#000000">In einem im Oktober 2000 erschienenen Artikel der U.S. News wurde die 
Heiligsprechung von Mutter Katharine Drexel, der Tochter eines Financiers aus Philadelphia, besprochen:<br>
"They called her the ‚millionaire nun‘ – an American heiress who gave 
up a $ 20 million family fortune to minister to American Indians and poor blacks." 
(J. Sheler, A Second Saint for America, in: U.S. News &amp; World Report, 9. Oktober 2000, 14)<br>
Besitzaufgabe als Barmherzigkeitspflege wurde bereits in der christlichen Urgemeinde praktiziert. 
Der Verzicht auf Reichtum begegnet uns auch in spätrömischer Zeit in Kreisen der 
christianisierten <i>nobilitas</i>. Mentoren und Lehrer wie Hieronymus, die mit einflussreichen 
Familien und adligen Zirkeln in Kontakt standen, haben diese nobiles in deren asketischem Streben 
angeleitet und sie angespornt, ihren Besitz zugunsten der Kirche zu veräussern. Der anonym 
verfasste Text De divitiis richtet sich an ein gebildetes Publikum der christianisierten nobilitas 
und thematisiert aus christlich-asketischer Perspektive Reichtum und den Umgang mit ihm.<br>
Der Verfasser ermahnt sein Publikum mit klaren Worten:<br><br>
<i>Nihil enim nobis proderit aut numerosae sobolis gloriosa progenies / aut per universas mundi 
partes longe lateque diffusa uberrimi caespitis locuples enim ac spatiosa possessio aut plurimarum 
aedium supera nimis et lata constructio aut pretiosarum vestium multiplex 
et gloriosa concinnitas aut dignitatum vel honorum ambitiosa iactantia, cum / extrema 
nos hora praevenerit, cum ambiguus omnibus finis advenerit </i>(…). (Div. 14<br><br>
Den reichen Adligen wird das folgende Bild vorgehalten:<br>

<i>Si vere fidi sumus, caelestibus nos remunerandos opibus et perpetuae possessionis 
sublimate ditandos, fastidiosa sit nobis terra post caelum et omnis temporalis et 
munda / na possessio ad aeternae illius beatitudinis comparationem quasi inutile 
stercorum sordeat purgamentum.</i> (Div. 20)<br><br>
Welche traditionellen Denk- und Handlungsmuster sucht der Verfasser von De divitiis 
zu ersetzen? Welche neuen Werte sollen an deren Stelle gelten?</font>

