<strong>Kapitel 1:</strong><br>

<p>Anfang und Ende der Spätantike</p>

<p>Die zeitliche Verortung der Spätantike als Epoche ist alles andere als eine eindeutige Sache. Suchen und diskutieren Sie in der Forschungsliteratur begegnende Epochendaten und die für die jeweilige Wahl ausschlaggebende Perspektive. In Standardwerken wie demjenigen von A.H.M. Jones ist eine solche Verortung und Charakterisierung der Spätantike als Epoche bereits in der Einleitung zu finden. Fragen Sie sich, wann, weshalb und vielleicht auch wo Sie selbst die Spätantike beginnen und enden lassen.</p>


